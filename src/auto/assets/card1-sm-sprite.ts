const getPxValue = (value: number) => {
    return value + "px";
};

export const getCard1SmSprite = (file) => {
    const fileConfig = config.frames[file];
    if (!fileConfig) {
        throw new Error("not found spritesheet fileConfig");
    }
    //70 39
    let width = 39;
    const { w, h, x, y } = fileConfig.frame;
    const originW = w;
    const originH = h;
    const scale = originW / originH;
    const height = width / scale;
    const scaleW = width / originW;
    const getValue = pxToRem;

    return {
        positionX: getValue(-x * scaleW),
        positionY: getValue(-y * scaleW),
        width: getValue(imageW * scaleW),
        height: getValue(imageH * scaleW),
    };
};
const rootValue = 192;

export const pxToRem = (pxValue: number) => {
    return pxValue / rootValue + "rem";
};
